        <strong>Inputting Percentages</strong><br>
        <p>There are three ways to input percentages into Squadron Manager, these are: as a percent,
        a fraction, or a decimal.<br>
        <strong>As a Percentage:</strong>To enter as a percentage, just type the percent as a number with the % sign
        (i.e. 80% would be 80).<br>
        <strong>As a Fraction:</strong> To enter as a fraction, just type the percent as the fraction of correct answers out
        of all the questions using a / (i.e. 15 correct out of 17 would be 15/17).<br>
        <strong>As a Decimal:</strong> To enter as a decimal just enter the decimal form of the percentage (i.e. 80% would be .8).
        <br><br> <strong>Note:</strong> Squadron Manager does not allow percentages over 100%.
        </p>
